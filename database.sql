        create database clickbuy_project;

        use clickbuy_project;

        create table category(
            category_id int primary key auto_increment,
            name varchar(100) not null,
            status int default 0,
            in_home tinyint default 0
        );

        create table product(
            product_id int primary key auto_increment,
            name varchar(100) not null,
            price float not null,
            org_price float,
            discount int,
            status varchar(300),
            category_id int,
            foreign key (category_id) references category (category_id)
        );

        create table product_image(
            prodimg_id int primary key auto_increment,
            image_url varchar(250),
            product_id int,
            foreign key (product_id) references product (product_id)
        );

        create table role(
            role_id int primary key auto_increment,
            name varchar(50) not null
        );

        create table user(
            user_id int primary key auto_increment,
            picture varchar(50) default "avatar-mac-dinh.png",
            name varchar(50) not null,
            phone_num varchar(50) not null,
            email varchar(100),
            password varchar(50) not null,
            address varchar(200),
            status int default 0,
            role_id int default 2,
            foreign key (role_id) references role (role_id)
        );

        create table cart(
            cart_id int primary key auto_increment,
            user_id int unique,
            created_at timestamp default current_timestamp,
            foreign key (user_id) references user (user_id)
        );

        create table cart_item(
            cart_id int,
            product_id int,
            quantity int default 1,
            add_at timestamp default current_timestamp,
            foreign key (cart_id) references cart (cart_id),
            foreign key (product_id) references product (product_id)
        );

        create table order_info(
            order_id int primary key auto_increment,    
            user_id int,
            customer_name varchar(50) not null,
            phone_num varchar(50) not null,
            email varchar(100) not null, 
            address varchar(200) not null,
            note varchar(500),
            payment_method int default 0,
            order_date date default current_date,
            foreign key (user_id) references user (user_id)
        );

        create table order_detail(
            product_id int,
            order_id int,
            price float,
            quantity int,
            deleted tinyint default 0,
            foreign key (product_id) references product (product_id),
            foreign key (order_id) references order_info (order_id)
        );

